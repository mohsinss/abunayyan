// Cost matrix (thousand SAR). From §05 of the mockup. "Other" row is aggregated;
// we represent it as a synthetic dept id to keep the matrix rectangular.
// Matrix order follows the mockup columns: wetico, nextracker, citiscape, atc,
// smc, aq, ksb, eaton, meac, holystar, abci, dct, plime, iics.
const MATRIX_ORDER = [
  "wetico",
  "nextracker",
  "citiscape",
  "atc",
  "smc",
  "aq",
  "ksb",
  "eaton",
  "meac",
  "holystar",
  "abci",
  "dct",
  "plime",
  "iics",
] as const;

function row(deptId: string, values: Array<number | null>): MatrixCell[] {
  return values.flatMap((v, i) => {
    if (v === null) return [];
    return [
      {
        departmentId: deptId,
        entityId: MATRIX_ORDER[i],
        amount: v * 1000, // input is thousand SAR; store raw SAR
      },
    ];
  });
}

